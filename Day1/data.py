# datatypes in python
# string
name = "Hello World"
print(name)
# integer
age = 25
print(age)
# float
pi = 3.14
print(pi)
# boolean
is_student = True
print(is_student)   
# list
fruits = ["apple", "banana", "cherry"]
print(fruits)
# tuple
coordinates = (10, 20)
print(coordinates)  
# dictionary
person = {"name": "Alice", "age": 30, "city": "New York"}
print(person)